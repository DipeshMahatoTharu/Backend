# What is Inheritance?

# Inheritance means:

# A child class can use the code of a parent class.

# Instead of writing the same code again and again, we inherit it.

# Real-life example 👨‍👦

# Imagine:

# Animal
# │
# ├── Dog
# ├── Cat
# └── Bird

# Every animal can:

# Eat 
# Sleep 

# So instead of writing eat() and sleep() inside every class, we write them once in Animal.

# Then:

# Dog inherits them.
# Cat inherits them.
# Bird inherits them.


